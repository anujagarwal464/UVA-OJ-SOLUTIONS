#include <algorithm>
#include <cstdio>
#include <cstring>
#include <vector>
#include <queue>
using namespace std;

typedef vector<int> vi;

#define MAX_V 110
#define INF 1000000000

int res[MAX_V][MAX_V], mf, f, s, t;
vi p;

void augment(int v, int minEdge) {
  if (v == s) { f = minEdge; return; }
  else if (p[v] != -1) { augment(p[v], min(minEdge, res[p[v]][v]));
                         res[p[v]][v] -= f; res[v][p[v]] += f; }
}

int main() {
  //freopen("output.txt","w",stdout);
  int V, k, vertex, weight,c,cas=0;

  while(scanf("%d",&V),V){
     scanf("%d %d %d",&s,&t,&c);
     memset(res, 0, sizeof res);

    for (int i = 0; i < c; i++) {
      scanf("%d %d %d",&k, &vertex, &weight);
      res[k][vertex]+=weight;
      res[vertex][k]+=weight;
    }

    mf = 0;
    while (1) {
      f = 0;
      vi dist(MAX_V, INF); dist[s] = 0;
      queue<int> q; q.push(s);
      p.assign(MAX_V, -1);
      while (!q.empty()) {
        int u = q.front(); q.pop();
        if (u == t) break;
        for (int v = 0; v < MAX_V; v++)
          if (res[u][v] > 0 && dist[v] == INF)
            dist[v] = dist[u] + 1, q.push(v), p[v] = u;
      }
      augment(t, INF);
      if (f == 0) break;
      mf += f;
    }
    printf("Network %d\n",++cas);
    printf("The bandwidth is %d.\n\n", mf);
 }
 return 0;
}

